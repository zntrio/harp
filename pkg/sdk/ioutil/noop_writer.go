package ioutil

import "io"

// NopCloser returns a WriteCloser with a no-op Close method wrapping
// the provided Writer w.
func NopCloserWriter(w io.Writer) io.WriteCloser {
	return nopCloser{w}
}

type nopCloser struct {
	io.Writer
}

func (nopCloser) Close() error { return nil }
